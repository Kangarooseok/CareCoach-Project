package com.carecoach.vo;


public class CategoryVO {

    private String name;
    private int writeAt;
    private int id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWriteAt() {
        return writeAt;
    }

    public void setWriteAt(int writeAt) {
        this.writeAt = writeAt;
    }


}
